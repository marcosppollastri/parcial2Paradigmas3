    private void jBotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotonAgregarActionPerformed
        Tramite tramite = new Tramite();
        
        String descripcion = txtDescripcion.getText();
        String prioridad = txtPrioridad.getText();
        int indexAgente = cmbAgente.getSelectedIndex();
        int indexBarrio = cmbBarrio.getSelectedIndex();
        Agente agente = ar.edu.unlar.ParcialNumero2.Principal.listaAgentes.get(indexAgente);
        Barrio barrio = ar.edu.unlar.ParcialNumero2.Principal.listaBarrios.get(indexBarrio);
       /* int indexReclamo = jListaReclamos.getSelectedIndex();
        Reclamo reclamo = ar.edu.unlar.ParcialNumero2.Principal.listaReclamos.get(indexReclamo);
        */
       int[] indexReclamos = jListaReclamos.getSelectedIndices();
       
       for(int i = 0; i< ar.edu.unlar.ParcialNumero2.Principal.listaReclamos.size(); i++){
           if(indexReclamos[i] == i){
               tramite.addReclamo(ar.edu.unlar.ParcialNumero2.Principal.listaReclamos.get(i));
           }
       }
        
        //carga
        tramite.setAgente(agente);
        tramite.setBarrio(barrio);
        tramite.setDescripcion(descripcion);
        tramite.setPrioridad(prioridad);
      //  tramite.addReclamo(reclamo);
        
        JOptionPane.showMessageDialog(rootPane, tramite.toString());
        
        
        
        
        
    }//GEN-LAST:event_jBotonAgregarActionPerformed

